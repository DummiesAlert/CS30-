package University_Staff;

public class UEmployee_One {









	private String firstName, lastName;


	private int Salary;


	


	


	public UEmployee_One(String Fn, String Ln, int Sal) {


		


		firstName = Fn;


		lastName = Ln;


		Salary = Sal;


		


	}


	


	


	


	public String toString() {


		return (firstName + " " + lastName + " $" + Salary);


	


 }


}



